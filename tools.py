def multiply(a: int, b: int) -> int:
    """
    This is multiply tool. Multiplies two numbers and returns the product.
    
    Args:
        a (int): The first number.
        b (int): The second number.
    
    Returns:
        int: The product of a and b.
    """
    print(f'***calling function for a={a}, b={b} ***')
    return a * b